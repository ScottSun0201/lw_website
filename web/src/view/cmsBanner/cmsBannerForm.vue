<template>
  <div>
    <div class="gva-form-box">
      <el-form :model="formData" ref="elFormRef" label-position="right" :rules="rule" label-width="80px">
        <el-form-item label="图片Title属性:" prop="title">
          <el-input v-model="formData.title" :clearable="true" placeholder="请输入" />
       </el-form-item>
        <el-form-item label="点击跳转路径:" prop="path">
          <el-input v-model="formData.path" :clearable="true" placeholder="请输入" />
       </el-form-item>
        <el-form-item label="图片:" prop="image">
          <SelectImage v-model="formData.image" file-type="image"/>
       </el-form-item>
        <el-form-item label="新窗口打开:" prop="newWindow">
          <el-switch v-model="formData.newWindow" active-color="#13ce66" inactive-color="#ff4949" active-text="是" inactive-text="否" clearable ></el-switch>
       </el-form-item>
        <el-form-item label="启用:" prop="open">
          <el-switch v-model="formData.open" active-color="#13ce66" inactive-color="#ff4949" active-text="是" inactive-text="否" clearable ></el-switch>
       </el-form-item>
        <el-form-item>
          <el-button type="primary" @click="save">保存</el-button>
          <el-button type="primary" @click="back">返回</el-button>
        </el-form-item>
      </el-form>
    </div>
  </div>
</template>

<script setup>
import {
  createCmsBanner,
  updateCmsBanner,
  findCmsBanner
} from '@/api/cmsBanner'

defineOptions({
    name: 'CmsBannerForm'
})

// 自动获取字典
import { getDictFunc } from '@/utils/format'
import { useRoute, useRouter } from "vue-router"
import { ElMessage } from 'element-plus'
import { ref, reactive } from 'vue'
import SelectImage from '@/components/selectImage/selectImage.vue'

const route = useRoute()
const router = useRouter()

const type = ref('')
const formData = ref({
            title: '',
            path: '',
            image: "",
            newWindow: false,
            open: false,
        })
// 验证规则
const rule = reactive({
})

const elFormRef = ref()

// 初始化方法
const init = async () => {
 // 建议通过url传参获取目标数据ID 调用 find方法进行查询数据操作 从而决定本页面是create还是update 以下为id作为url参数示例
    if (route.query.id) {
      const res = await findCmsBanner({ ID: route.query.id })
      if (res.code === 0) {
        formData.value = res.data.recmsBanner
        type.value = 'update'
      }
    } else {
      type.value = 'create'
    }
}

init()
// 保存按钮
const save = async() => {
      elFormRef.value?.validate( async (valid) => {
         if (!valid) return
            let res
           switch (type.value) {
             case 'create':
               res = await createCmsBanner(formData.value)
               break
             case 'update':
               res = await updateCmsBanner(formData.value)
               break
             default:
               res = await createCmsBanner(formData.value)
               break
           }
           if (res.code === 0) {
             ElMessage({
               type: 'success',
               message: '创建/更改成功'
             })
           }
       })
}

// 返回按钮
const back = () => {
    router.go(-1)
}

</script>

